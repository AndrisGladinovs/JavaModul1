package day12.homework;

public class EndsWithStringSuff {
    public static void main(String[] args) {
        // Funkcija endsWith(String suffix) proverjaet zakachivaetsja
        // li strochka opredelennim slovom ili simvolom
        String str = "Today is Wednesday";
        if (str.endsWith("Wednesday")) {
            System.out.println("The given String ends with Wednesday");
        }

    }
}
